package com.example.islamdamai.model;

public class Kisah {
    public String nama;
    public String desk;
    public String mujizat;
    public int gambar;
    public int banner;

    public String getMujizat() {
        return mujizat;
    }

    public void setMujizat(String mujizat) {
        this.mujizat = mujizat;
    }


    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getDesk() {
        return desk;
    }

    public void setDesk(String desk) {
        this.desk = desk;
    }

    public int getGambar() {
        return gambar;
    }

    public void setGambar(int gambar) {
        this.gambar = gambar;
    }

    public int getBanner() {
        return banner;
    }

    public void setBanner(int banner) {
        this.banner = banner;
    }


}